    private void cadastroEndereco(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastroEndereco
    String codendStr = jTextcodend.getText();
    String numeroStr = jTextnumero.getText();
    String cep = jTextcep.getText();
    String bairro = jTextbairro.getText();
    String estado = jTextestado.getText();
    String rua = jTextrua.getText();
    
    
     if (codendStr.isEmpty() || numeroStr.isEmpty() || cep.isEmpty() || bairro.isEmpty() || estado.isEmpty() || rua.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");

    }
     int codend;
     int numero;
         try {
        codend = Integer.parseInt(codendStr);
        numero = Integer.parseInt(numeroStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Cód. end e numero devem ser números válidos!");
        return;
    }

     enderecoM endereco = new enderecoM(codend, numero, cep, bairro, estado, rua);
     
     String  resposta = control.CadastroEndereco(endereco);
     
    if ("OK".equals(resposta)) {
        JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");

        // Limpa os campos após o cadastro
        jTextcodend.setText("");
        jTextnumero.setText("");
        jTextcep.setText("");
        jTextbairro.setText("");
        jTextestado.setText("");
        jTextrua.setText("");
    }
    }//GEN-LAST:event_cadastroEndereco

