    private void ConsultaEndereco(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultaEndereco
        try {
        // Obtenha o modelo da tabela
        DefaultTableModel tabelaModelo = (DefaultTableModel) jTabelaendereco.getModel();

        // Limpa os dados existentes na tabela
        tabelaModelo.setRowCount(0);

        // Coleta o filtro de nome da interface gráfica (opcional)
        String codendStr = jTextcodend.getText();
        
     int codend;  

    try {
        // Tenta converter o valor para inteiro
        codend = Integer.parseInt(codendStr);
    } catch (NumberFormatException e) {
        // Se a conversão falhar, exibe uma mensagem de erro
        JOptionPane.showMessageDialog(null, "O codigo de endereço deve ser um número valido!");
        return;
    }

        // Consulta no banco de dados
        List<enderecoM> resultados = control.ConsultaEndereco(codend); // Ajuste o método conforme seu controlador

        // Preenche a tabela com os resultados
        for (enderecoM dados : resultados) {
            Object[] linha = {
                dados.getCodend(),
                dados.getNumero(),
                dados.getCep(),
                dados.getBairro(),
                dados.getEstado(),
                dados.getRua()
            };
            tabelaModelo.addRow(linha);
        }

        // Caso não existam resultados
        if (resultados.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum registro encontrado!");
        }
    } catch (Exception erro) {
        erro.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao consultar dados: " + erro.getMessage());
    }
    }//GEN-LAST:event_ConsultaEndereco

